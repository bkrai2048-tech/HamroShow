const NEPALI_NUMERALS = ["०", "१", "२", "३", "४", "५", "६", "७", "८", "९"]

export function toNepaliNumeral(value: number | string) {
    return String(value)
        .split("")
        .map((digit) => {
            const index = Number(digit)
            return Number.isNaN(index) ? digit : NEPALI_NUMERALS[index]
        })
        .join("")
}

export function getAvailableYears() {
    return Object.keys(BS_CALENDAR_DATA)
        .map(Number)
        .sort((a, b) => a - b)
}

export function getDaysInBSMonth(year: number, month: number) {
    return BS_CALENDAR_DATA[year]?.[month - 1] || 0
}

function getTotalDaysInBsYear(year: number) {
    return BS_CALENDAR_DATA[year]?.reduce((sum, days) => sum + days, 0) || 0
}

function countAdDays(from: BSDate, to: BSDate) {
    const fromDate = Date.UTC(from.year, from.month - 1, from.day)
    const toDate = Date.UTC(to.year, to.month - 1, to.day)
    return Math.round((toDate - fromDate) / 86400000)
}

function countBsDays(from: BSDate, to: BSDate) {
    let totalDays = 0
    for (let year = from.year; year < to.year; year++) totalDays += getTotalDaysInBsYear(year)
    for (let month = 0; month < from.month - 1; month++) totalDays -= BS_CALENDAR_DATA[from.year][month]
    totalDays -= from.day
    for (let month = 0; month < to.month - 1; month++) totalDays += BS_CALENDAR_DATA[to.year][month]
    totalDays += to.day
    return totalDays
}

function addAdDays(date: BSDate, days: number): BSDate {
    const target = new Date(Date.UTC(date.year, date.month - 1, date.day))
    target.setUTCDate(target.getUTCDate() + days)
    return { year: target.getUTCFullYear(), month: target.getUTCMonth() + 1, day: target.getUTCDate() }
}

function addBsDays(date: BSDate, days: number): BSDate {
    let { year, month, day } = { ...date }
    day += days

    while (day > 0 && BS_CALENDAR_DATA[year] && day > BS_CALENDAR_DATA[year][month - 1]) {
        day -= BS_CALENDAR_DATA[year][month - 1]
        month++
        if (month > 12) {
            month = 1
            year++
        }
    }

    return { year, month, day }
}

export function bsToAD(bsDate: BSDate) {
    if (!BS_CALENDAR_DATA[bsDate.year]) return null
    const days = countBsDays(BS_REFERENCE, bsDate)
    const adDate = addAdDays(AD_REFERENCE, days)
    return new Date(adDate.year, adDate.month - 1, adDate.day)
}

export function adToBS(adDate: Date | string): BSDate {
    const date = typeof adDate === "string" ? new Date(adDate) : adDate
    if (!(date instanceof Date) || Number.isNaN(date.getTime())) return { year: 2082, month: 1, day: 1 }

    const days = countAdDays(AD_REFERENCE, { year: date.getFullYear(), month: date.getMonth() + 1, day: date.getDate() })
    const bsDate = addBsDays(BS_REFERENCE, days)
    if (!BS_CALENDAR_DATA[bsDate.year]) return { year: 2082, month: 1, day: 1 }
    return bsDate
}

export function formatBSDate(bsDate: BSDate, locale: "en" | "ne" = "ne") {
    const month = NEPALI_MONTHS[locale][bsDate.month - 1]
    if (locale === "ne") return `${toNepaliNumeral(bsDate.year)} ${month} ${toNepaliNumeral(bsDate.day)}`
    return `${bsDate.year} ${month} ${bsDate.day}`
}
